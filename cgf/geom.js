function vec2(_x, _y) {
	return {
		x: _x, y: _y,
	}
}

function vec3(_x, _y, _z) {
	return {
		x: _x, y: _y, z: _z,
	}
}

function vec4(_x, _y, _z, _w) {
	return {
		x: _x, y: _y, z: _z, w: _w,
	}
}

function Rect(_x, _y, _width, _height) {
    return {
        x: _x, y: _y, width: _width, height: _height,
    }
}